const GUIDE_DUMMY_TOTAL = 42
const GUIDE_DUMMY_PAGE_SIZE = 6

function guidePageOf(index) {
  return Math.floor(index / GUIDE_DUMMY_PAGE_SIZE) + 1
}

function buildGuideEntries(seeds, total, decorate = () => ({})) {
  return Array.from({ length: total }, (_, index) => {
    const seed = seeds[index % seeds.length]
    const page = guidePageOf(index)
    const number = index + 1
    const name = index < seeds.length ? seed.name : `${seed.name} ${number}`

    return {
      ...seed,
      ...decorate(seed, index, page),
      name,
      summary: `${seed.summary} 페이지 ${page} 더미 데이터입니다.`,
      tags: [...seed.tags, `P${page}`],
    }
  })
}

