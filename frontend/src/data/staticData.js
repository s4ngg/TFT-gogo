function patch(id, category, target, type, impact, summary, before, after, tags) {
  return { id, category, target, type, impact, summary, before, after, tags }
}

const PATCH_DUMMY_TOTAL = 35
const PATCH_DUMMY_PAGE_SIZE = 5

function buildPatchChanges(seeds, total, idOffset = 0) {
  return Array.from({ length: total }, (_, index) => {
    const seed = seeds[index % seeds.length]
    const id = idOffset + index + 1
    const page = Math.floor(index / PATCH_DUMMY_PAGE_SIZE) + 1
    const target = index < seeds.length ? seed.target : `${seed.target} ${index + 1}`
    return patch(
      id,
      seed.category,
      target,
      seed.type,
      seed.impact,
      `${seed.summary} 페이지 ${page} 더미 변경사항입니다.`,
      seed.before,
      seed.after,
      [...seed.tags, `P${page}`],
    )
  })
}

